package _2_solve._2_6_leet_code._2_6_1_problem_set._2_6_1_3_hard._2_6_1_3_25_reverse_nodes_in_k_group;

class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(final int VALUE) {
        this.val = VALUE;
    }

    ListNode(final int VALUE, final ListNode NEXT) {
        this.val = VALUE;
        this.next = NEXT;
    }
}