public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(final int VALUE) {
        this.val = VALUE;
    }

    TreeNode(final int VALUE, final TreeNode LEFT, final TreeNode RIGHT) {
        this.val = VALUE;
        this.left = LEFT;
        this.right = RIGHT;
    }
}