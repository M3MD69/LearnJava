package _2_solve._2_1_w3schools._2_1_1_how_to._2_1_1_16_square_root;

public class SquareRoot {
    public static void main(final String[] PARAMETERS) {
        System.out.print(getSquareRoot(4));
    }

    private static double getSquareRoot(final int NUMBER) {
        return Math.sqrt(NUMBER);
    }
}