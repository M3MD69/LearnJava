package _2_solving._2_6_leet_code._2_6_1_problem_set._2_6_1_2_medium._2_6_1_2_19_remove_n_th_node_from_end_of_list;

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(final int VALUE) {
        this.val = VALUE;
    }

    ListNode(final int VALUE, final ListNode NEXT) {
        this.val = VALUE;
        this.next = NEXT;
    }
}