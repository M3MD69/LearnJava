class ListNode {
    int val;
    ListNode next;

    ListNode() {

    }

    ListNode(final int VALUE) {
        this.val = VALUE;
    }

    ListNode(final int VALUE, final ListNode NEXT) {
        this.val = VALUE;
        this.next = NEXT;
    }
}