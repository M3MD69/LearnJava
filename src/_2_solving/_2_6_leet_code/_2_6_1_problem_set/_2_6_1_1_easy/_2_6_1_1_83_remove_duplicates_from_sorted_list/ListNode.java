package _2_solving._2_6_leet_code._2_6_1_problem_set._2_6_1_1_easy._2_6_1_1_83_remove_duplicates_from_sorted_list;

class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(final int VALUE) {
        this.val = VALUE;
    }

    ListNode(final int VALUE, final ListNode NEXT) {
        this.val = VALUE;
        this.next = NEXT;
    }
}