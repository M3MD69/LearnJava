package _2_solving._2_6_leet_code._2_6_2_problem_set._2_6_2_1_easy._2_6_2_1_21_merge_two_sorted_lists;

class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(final int VALUE) {
        this.val = VALUE;
    }

    ListNode(final int VALUE, final ListNode NEXT) {
        this.val = VALUE;
        this.next = NEXT;
    }
}