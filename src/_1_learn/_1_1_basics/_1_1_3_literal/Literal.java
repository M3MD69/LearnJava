package _1_learn._1_1_basics._1_1_3_literal;

public class Literal {
    public static void main(final String[] ARGUMENTS) {
        int hexadecimalValue = 0xF; // (F) In Hexadecimal = 15
        long decimalValue = 15L; // (15) In Decimal = 15
        short octaValue = 017; // (17) In Octa = 15
        byte binaryValue = 0b1111; // (1111) In Binary = 15
        double binaryValue2 = 0b1111; // (1111) In Binary = 15
        float binaryValue3 = 0b1111; // (1111) In Binary = 15

        /* Not important Use int, You can use any data type of number! */
    }
}